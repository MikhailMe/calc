#pragma once

// сложение
double addition(int a, int b) {
    return (double) a + b;
}

// вычитание
double subtraction(int a, int b) {
    return (double) a - b;
}

// умножение
double multiplication(int a, int b) {
    return (double) a * b;
}

// деление
double divide(int a, int b) {
    return (double) a / b;
}